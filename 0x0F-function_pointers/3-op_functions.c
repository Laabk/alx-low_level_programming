#include "3-calc.h"

/**
 * op_sub - difference of two numbers
 * @a: first number
 * @b: second number
 * Return: difference of a and b
 */

int op_sub(int a, int b)

{
	return (a - b);
}

/**
 * op_add - calculates the sum of two numbers
 * @a: first number
 * @b: second number
 * Return: sum of a and b
 */

int op_add(int a, int b)

{
	return (a + b);
}

/**
 * op_mul - calculates the product of two numbers
 * @a: first number
 * @b: second number
 * Return: product of a and b
 */

int op_mul(int a, int b)

{
	return (a * b);
}

/**
 * op_mod - calculates modulos of two numbers
 * @a: first number
 * @b: second number
 * Return: remainder of a divident b
*/

int op_mod(int a, int b)

{
	return (a % b);
}

/**
 * op_div - calculates division of two numbers
 * @a: first number
 * @b: second number
 * Return: result after the division of a and b
 */

int op_div(int a, int b)

{
	return (a / b);
}
